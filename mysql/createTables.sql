CREATE TABLE invoice (invoiceId INT AUTO_INCREMENT PRIMARY KEY, 
companyName VARCHAR(255),
contactName VARCHAR(255),
link VARCHAR(255),
dueDate VARCHAR(255),
issuedDate VARCHAR(255) );

CREATE TABLE contact (
contactId INT AUTO_INCREMENT PRIMARY KEY,
companyId INT,
contactType VARCHAR(255),
companyName VARCHAR(255),
title VARCHAR(255),
firstName VARCHAR(255),
lastName VARCHAR(255),
contactEmail VARCHAR(255),
Country VARCHAR(255),
contactPhoneNumber VARCHAR(255),
Address1 VARCHAR(255),
Address2 VARCHAR(255),
Address3 VARCHAR(255),
Town VARCHAR(255),
County VARCHAR(255),
postalCode VARCHAR(255));

CREATE TABLE company (
companyId INT AUTO_INCREMENT PRIMARY KEY,
companyName VARCHAR(255),
chairPersonName VARCHAR(255),
chairPersonEmail VARCHAR(255),
chairPersonNumber VARCHAR(255),
logoUrl VARCHAR(255),
companyEmail VARCHAR(255),
Country VARCHAR(255),
companyPhoneNumber VARCHAR(255),
Address1 VARCHAR(255),
Address2 VARCHAR(255),
Address3 VARCHAR(255),
Town VARCHAR(255),
County VARCHAR(255),
postalCode VARCHAR(255),
vat VARCHAR(255),
hasBankDetails BOOLEAN,
bankName VARCHAR(255),
accountName VARCHAR(255), 
accountNumber VARCHAR(255),
bicCode VARCHAR(255),
swiftCode VARCHAR(255), 
sortCode VARCHAR(255));
